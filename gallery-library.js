/* Assign a filter category based on the filename. */
const getFilterCategory = (filename) => {
  const f = filename.toLowerCase();
  if (/workshop|carpentry|craftsman|stacked|packaged|door-installation|window-install|wood-deck|production-door|wood-veneer/.test(f)) return "production";
  if (/factory/.test(f) && !/factory-black-door-frames/.test(f)) return "production";
  if (/garage|roller/.test(f)) return "garage";
  if (/panoramic|folding-glass|glass-terrace|glass-buildings/.test(f)) return "glass";
  if (/installed-wood-glass/.test(f)) return "glass";
  if (/model-gray-glass-panel/.test(f)) return "glass";
  if (/^archive-commercial/.test(f)) return "commercial";
  if (/security|exterior-wood-security|showroom-pivot|showroom-security/.test(f)) return "security";
  if (/interior|hallway|corridor|shutter-door|bedroom|white-panel-door/.test(f)) return "interior";
  if (/product-light-wood-interior|product-warm-gray-interior|product-white-wood-interior/.test(f)) return "interior";
  if (/model-gray-classic-interior|catalog-pale-wood-interior|catalog-walnut-interior|catalog-white-interior|catalog-white-black-interior/.test(f)) return "interior";
  if (/catalog-honey-wood|catalog-light-gray|catalog-minimal-white/.test(f)) return "interior";
  if (/floating-staircase|staircase/.test(f)) return "interior";
  if (/classic-white-door-blue-room|white-interior-door-room|white-interior-door-open/.test(f)) return "interior";
  return "entrance";
};

const FILTER_LABELS = {
  all:        "Të gjitha",
  entrance:   "Hyrje",
  interior:   "Brendshme",
  security:   "Sigurie",
  commercial: "Komercial",
  glass:      "Xham",
  garage:     "Garazh",
  production: "Prodhim",
};

const archiveGallery = document.querySelector("[data-archive-gallery]");
if (archiveGallery) {
  const titleFromFilename = (filename) => filename
    .replace(/^uploads\/uploads\/\d{4}\/\d{2}\//, "")
    .replace(/^wordpress-archive\/wp-/, "")
    .replace(/^archive-/, "")
    .replace(/\.(?:jpe?g|png|webp)$/i, "")
    .split("-")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");

  const fragment = document.createDocumentFragment();
  falArchiveGallery.forEach((filename, index) => {
    const article = document.createElement("article");
    const image = document.createElement("img");
    const title = document.createElement("h3");
    const number = document.createElement("span");
    const readableTitle = titleFromFilename(filename);
    const cat = getFilterCategory(filename);
    image.src = `images/${filename}`;
    image.alt = readableTitle;
    image.loading = "lazy";
    image.decoding = "async";
    image.addEventListener("error", () => {
      article.classList.add("is-missing-image");
      image.remove();
    }, { once: true });
    title.textContent = readableTitle;
    number.textContent = String(index + 1).padStart(3, "0");
    article.dataset.filterCat = cat;
    article.append(image, number, title);
    fragment.append(article);
  });
  archiveGallery.append(fragment);

  /* Inject filter bar above the grid */
  const filterBar = document.createElement("div");
  filterBar.className = "fal-gallery-filter-bar";
  filterBar.setAttribute("aria-label", "Filtro galerine");
  filterBar.setAttribute("role", "group");

  let activeCat = "all";
  const allArticles = [...archiveGallery.querySelectorAll("article")];
  const visibleCounter = document.querySelector("[data-gallery-visible]");
  const totalCounter = document.querySelector("[data-gallery-total]");
  if (totalCounter) totalCounter.textContent = String(allArticles.length);
  if (visibleCounter) visibleCounter.textContent = String(allArticles.length);

  const applyFilter = (cat) => {
    activeCat = cat;
    let visibleCount = 0;
    filterBar.querySelectorAll("button").forEach((btn) => {
      const isActive = btn.dataset.filterKey === cat;
      btn.classList.toggle("is-active", isActive);
      btn.setAttribute("aria-pressed", String(isActive));
    });
    allArticles.forEach((article) => {
      const show = cat === "all" || article.dataset.filterCat === cat;
      article.hidden = !show;
      if (show) visibleCount += 1;
    });
    if (visibleCounter) visibleCounter.textContent = String(visibleCount);
  };

  Object.entries(FILTER_LABELS).forEach(([key, label]) => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.dataset.filterKey = key;
    btn.textContent = label;
    btn.setAttribute("aria-pressed", String(key === "all"));
    btn.className = key === "all" ? "is-active" : "";
    btn.addEventListener("click", () => applyFilter(key));
    filterBar.append(btn);
  });

  archiveGallery.before(filterBar);
}

